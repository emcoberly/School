/*==========================================================================
 * Function: mean(int[], int)
 *
 * Precondition: int[] is an integer array of positive length given in the
 *               second parameter
 * Postcondition: the value of the mean (or average) of the values is returned
 */
double mean(int a[], int size);

/*==========================================================================
 * Function: median(int[], int)
 *
 * Precondition: int[] is an array integer of the positive length given in
 *               the 2nd parameter
 * Postcondition: the value of the median is returned
 */
double median(int a[], int size);
